package example;

import java.util.*;

public class Main {

    public static int solution(int a, int b){
        return a - b;
    }

    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
    
        int a = sc.nextInt();
        int b = sc.nextInt();

        System.out.println(solution(a, b));
        
        sc.close();
    }
    
}


